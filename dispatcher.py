def _make_askpass():
    """Write a GIT_ASKPASS script that reads creds from env vars. Returns path."""
    if not GITHUB_TOKEN:
        return None
    try:
        fd, path = tempfile.mkstemp(suffix=".sh", prefix="git_askpass_")
        with os.fdopen(fd, "w") as f:
            f.write('#!/bin/sh\n'
                    'case "$1" in\n'
                    '  *Username*) echo "$_GIT_USER" ;;\n'
                    '  *)          echo "$_GIT_PASS" ;;\n'
                    'esac\n')
        os.chmod(path, stat.S_IRWXU)
        return path
    except Exception as e:
        log.warning(f"Could not create askpass script: {e}")
        return None


ASKPASS = _make_askpass()

