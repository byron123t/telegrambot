# Tools auto-approved without prompting (matches --allowedTools passed to claude)
_CLAUDE_ALLOWED_TOOLS = (
    "Bash,Read,Write,Edit,Glob,Grep,LS,"
    "WebFetch,WebSearch,"
    "NotebookRead,NotebookEdit,"
    "TodoRead,TodoWrite"
)


def setup_claude_hooks():
    """
    Write permission_hook.py and register it as a Claude Code PreToolUse hook.
    The hook auto-approves tools in _CLAUDE_ALLOWED_TOOLS; for anything else it
    sends a Telegram message with Allow/Deny buttons and waits for the tap.
    Communication with the dispatcher uses temp files (/tmp/claude_perm_<id>.*).
    """
    if not NOTIFY_CHAT_ID:
        log.warning("No notify_chat_id configured — Claude permission hook disabled.")
        return

    hook_path = Path(__file__).parent / "permission_hook.py"
    allowed_set = "{" + ",".join(f'"{t.strip()}"' for t in _CLAUDE_ALLOWED_TOOLS.split(",")) + "}"

    hook_script = f'''#!/usr/bin/env python3
"""Claude Code PreToolUse permission hook — auto-generated by dispatcher.py"""
import json, os, sys, time, urllib.request, uuid

ALLOWED = {allowed_set}

data      = json.load(sys.stdin)
tool      = data.get("tool_name", "")
tool_in   = json.dumps(data.get("tool_input", {{}}), indent=2)[:400]

if tool in ALLOWED:
    print(json.dumps({{"decision": "approve"}}))
    sys.exit(0)

TOKEN   = "{TELEGRAM_TOKEN}"
CHAT_ID = {NOTIFY_CHAT_ID}

req_id    = uuid.uuid4().hex[:8]
req_file  = f"/tmp/claude_perm_{{req_id}}.request"
resp_file = f"/tmp/claude_perm_{{req_id}}.response"
open(req_file, "w").close()

payload = json.dumps({{
    "chat_id": CHAT_ID,
    "text": f"🔐 *Permission Request*\\n\\nTool: `{{tool}}`\\n```\\n{{tool_in}}\\n```",
    "parse_mode": "Markdown",
    "reply_markup": {{"inline_keyboard": [[
        {{"text": "✅ Allow", "callback_data": f"perm_allow:{{req_id}}"}},
        {{"text": "❌ Deny",  "callback_data": f"perm_deny:{{req_id}}"}}
    ]]}}
}}).encode()

try:
    urllib.request.urlopen(urllib.request.Request(
        f"https://api.telegram.org/bot{{TOKEN}}/sendMessage",
        data=payload, headers={{"Content-Type": "application/json"}}), timeout=10)
except Exception as e:
    os.unlink(req_file)
    print(json.dumps({{"decision": "deny", "reason": f"Telegram unreachable: {{e}}"}}))
    sys.exit(0)

for _ in range(30):
    time.sleep(2)
    if os.path.exists(resp_file):
        decision = open(resp_file).read().strip()
        os.unlink(resp_file)
        os.unlink(req_file)
        print(json.dumps({{"decision": "approve" if decision == "allow" else "deny",
                           "reason": "" if decision == "allow" else "Denied via Telegram"}}))
        sys.exit(0)

os.unlink(req_file)
print(json.dumps({{"decision": "deny", "reason": "Permission request timed out (60s)"}}))
'''

    hook_path.write_text(hook_script)
    hook_path.chmod(0o755)

    # Merge into ~/.claude/settings.json, preserving any existing settings
    settings_path = Path.home() / ".claude" / "settings.json"
    settings_path.parent.mkdir(exist_ok=True)
    try:
        settings = json.loads(settings_path.read_text()) if settings_path.exists() else {}
    except Exception:
        settings = {}

    settings.setdefault("hooks", {})
    settings["hooks"]["PreToolUse"] = [{
        "matcher": ".*",
        "hooks": [{"type": "command", "command": f"python3 {hook_path}"}]
    }]
    settings_path.write_text(json.dumps(settings, indent=2))
    log.info(f"Claude permission hook registered → {hook_path}")


