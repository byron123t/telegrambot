#!/usr/bin/env python3
"""
Phone → Desktop AI Agent Dispatcher
=====================================
Telegram bot that routes commands to AI agents on your desktop.

Workflow:
  1. proj              — pick an active project directory
  2. claude: <prompt>  — run Claude Code in active project
     cursor: <prompt>  — run Cursor in active project
  3. gh pull           — git pull (authenticated)
     gh push           — git add -A, Claude-generated commit msg, push
     gh reset          — pick a commit to reset to
     gh init <url>     — git init + set remote origin

.env (next to this script):
  GITHUB_TOKEN=ghp_...
  GITHUB_EMAIL=you@example.com
  GITHUB_NAME=Your Name     # optional, defaults to email prefix

Other:
  mkdir: <name> [--public|--private]  — create dir (optionally as GitHub repo)
  status
"""

import asyncio
import json
import logging
import os
import platform
import re
import stat
import sys
import tempfile
from datetime import datetime
from pathlib import Path

CONFIG_PATH = Path(__file__).parent / "config.json"
ENV_PATH    = Path(__file__).parent / ".env"


def load_config():
    if not CONFIG_PATH.exists():
        print(f"ERROR: {CONFIG_PATH} not found. Copy config.example.json → config.json")
        sys.exit(1)
    with open(CONFIG_PATH) as f:
        return json.load(f)


def load_env_file() -> dict:
    result = {}
    if ENV_PATH.exists():
        for line in ENV_PATH.read_text().splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, v = line.split("=", 1)
                result[k.strip()] = v.strip().strip('"').strip("'")
    return result


CFG              = load_config()
TELEGRAM_TOKEN   = CFG["telegram_bot_token"]
ALLOWED_CHAT_IDS = set(CFG.get("allowed_chat_ids", []))
MACHINE_NAME     = CFG.get("machine_name", platform.node())
WORK_DIR         = Path(CFG.get("default_work_dir", str(Path.home())))
MAX_OUTPUT_LEN   = CFG.get("max_output_length", 3500)
# Chat to receive permission requests (first allowed_chat_id, or explicit notify_chat_id)
NOTIFY_CHAT_ID   = CFG.get("notify_chat_id") or next(iter(sorted(ALLOWED_CHAT_IDS)), None)

_ENV             = load_env_file()
GITHUB_TOKEN     = _ENV.get("GITHUB_TOKEN", "")
GITHUB_EMAIL     = _ENV.get("GITHUB_EMAIL", "")
GITHUB_NAME      = _ENV.get("GITHUB_NAME", GITHUB_EMAIL.split("@")[0] if GITHUB_EMAIL else "bot")

logging.basicConfig(level=logging.INFO,
                    format=f"[{MACHINE_NAME}] %(asctime)s %(levelname)s: %(message)s")
log = logging.getLogger(__name__)

# Per-chat state
active_dirs: dict = {}  # chat_id → abs path str
pending:     dict = {}  # chat_id → {"type": "proj"|"reset"|"init", ...}

ALIASES = {"cc": "claude", "cur": "cursor", "github": "gh", "p": "proj"}

# ---------------------------------------------------------------------------
# Session persistence  (chat_id, work_dir) → claude session_id
