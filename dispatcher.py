async def main():
    from telegram import Update, InlineKeyboardButton, InlineKeyboardMarkup
    from telegram.ext import (Application, MessageHandler, CommandHandler,
                               CallbackQueryHandler, filters, ContextTypes)

    app = Application.builder().token(TELEGRAM_TOKEN).build()

    def authorized(update: Update) -> bool:
        return not ALLOWED_CHAT_IDS or update.effective_chat.id in ALLOWED_CHAT_IDS

    async def handle_start(update: Update, context: ContextTypes.DEFAULT_TYPE):
        auth = "✅ token loaded" if GITHUB_TOKEN else "⚠️ no token \\(.env\\)"
        await update.message.reply_text(
            f"🖥 *{MACHINE_NAME}* ready  \\(GitHub: {auth}\\)\n\n"
            f"1\\. `proj` — pick a project\n"
            f"2\\. `claude: prompt` / `cursor: prompt`\n"
            f"3\\. `gh pull` / `gh push` / `gh reset` / `gh init <url>`\n\n"
            f"`mkdir: name \\[\\-\\-public|\\-\\-private\\]`\n"
            f"`status`",
            parse_mode="MarkdownV2"
        )

    async def handle_status(update: Update, context: ContextTypes.DEFAULT_TYPE):
        if not authorized(update):
            return
        wd   = active_dirs.get(update.effective_chat.id)
        proj = f"`{Path(wd).name}`" if wd else "_(none)_"
        auth = f"`{GITHUB_EMAIL}`" if GITHUB_EMAIL else "_(not configured)_"
        await update.message.reply_text(
            f"🖥 *{MACHINE_NAME}*  {platform.system()} {platform.release()}\n"
            f"📂 `{WORK_DIR}`\n"
            f"📌 Active: {proj}\n"
            f"🔑 GitHub: {auth}\n"
            f"⏰ {datetime.now():%H:%M:%S}",
            parse_mode="Markdown"
        )

    async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
        chat_id = update.effective_chat.id
        if not authorized(update):
            await update.message.reply_text(
                f"⛔ Unauthorized. Chat ID: `{chat_id}`", parse_mode="Markdown")
            return

        text = (update.message.text or "").strip()

        # ── Intercept pending "init" waiting for a URL ──────────────────────
        p = pending.get(chat_id, {})
        if p.get("type") == "init":
            url = text
            pending.pop(chat_id)
            if url.startswith("http"):
                ack = await update.message.reply_text("⏳ Initialising…")
                result = await run_gh_init(url, p["work_dir"])
                await ack.edit_text(result)
            else:
                await update.message.reply_text("❌ Doesn't look like a URL. Run `gh init` again.")
            return

        cmd, args = parse_message(text)

        if cmd == "status":
            await handle_status(update, context)
            return

        # ── proj ─────────────────────────────────────────────────────────────
        if cmd == "proj":
            subdirs = list_subdirs()
            if not subdirs:
                await update.message.reply_text(
                    f"No subdirectories found in `{WORK_DIR}`.", parse_mode="Markdown")
                return
            pending[chat_id] = {"type": "proj", "subdirs": subdirs}
            rows = ([[InlineKeyboardButton(
                         f"/ {WORK_DIR.name}/ (root)", callback_data="proj:0")]] +
                    [[InlineKeyboardButton(f"📁 {d}", callback_data=f"proj:{i+1}")]
                     for i, d in enumerate(subdirs)])
            await update.message.reply_text("📂 Select project:",
                                            reply_markup=InlineKeyboardMarkup(rows))
            return

        # ── commands requiring an active project ─────────────────────────────
        wd = active_dirs.get(chat_id)
        if cmd in ("claude", "cursor", "gh") and not wd:
            await update.message.reply_text(
                "📂 No active project. Send `proj` to pick one.", parse_mode="Markdown")
            return

        if cmd == "claude":
            if not args:
                await update.message.reply_text("Usage: `claude: <prompt>`", parse_mode="Markdown")
                return
            # claude new — clear session for this project
            if args.strip().lower() == "new":
                sessions.pop((chat_id, wd), None)
                _save_sessions(sessions)
                await update.message.reply_text(
                    f"🔄 Claude session cleared for `{Path(wd).name}`.", parse_mode="Markdown")
                return
            ack = await update.message.reply_text(
                f"⏳ Claude in `{Path(wd).name}`…", parse_mode="Markdown")
            result = truncate(await run_claude(args, wd, chat_id))
            sid = sessions.get((chat_id, wd))
            header = f"✅ *{MACHINE_NAME}* — Claude (`{Path(wd).name}`)"
            header += f"  _{sid[:8]}_" if sid else ""
            try:
                await ack.edit_text(f"{header}\n\n{result}", parse_mode="Markdown")
            except Exception:
                await ack.edit_text(f"✅ {MACHINE_NAME} — Claude\n\n{result}")
            return

        if cmd == "cursor":
            if not args:
                await update.message.reply_text("Usage: `cursor: <prompt>`", parse_mode="Markdown")
                return
            ack = await update.message.reply_text(
                f"⏳ Cursor in `{Path(wd).name}`…", parse_mode="Markdown")
            result = truncate(await run_cursor(args, wd))
            try:
                await ack.edit_text(
                    f"✅ *{MACHINE_NAME}* — Cursor (`{Path(wd).name}`)\n\n{result}",
                    parse_mode="Markdown")
            except Exception:
                await ack.edit_text(f"✅ {MACHINE_NAME} — Cursor\n\n{result}")
            return

        if cmd == "gh":
            op = args.split()[0].lower() if args.split() else ""

            # gh init — with or without URL
            if op == "init":
                url = args.split(None, 1)[1].strip() if len(args.split(None, 1)) > 1 else ""
                if url:
                    ack = await update.message.reply_text("⏳ Initialising…")
                    result = await run_gh_init(url, wd)
                    await ack.edit_text(result)
                else:
                    pending[chat_id] = {"type": "init", "work_dir": wd}
                    await update.message.reply_text(
                        "🔗 Paste your GitHub repo URL:\n"
                        "_(e.g. `https://github.com/you/repo.git`)_",
                        parse_mode="Markdown")
                return

            # gh reset without hash → commit picker
            if op == "reset" and not args.split()[1:]:
                commits = await fetch_recent_commits(wd)
                if not commits:
                    await update.message.reply_text("No commits found in this repo.")
                    return
                pending[chat_id] = {"type": "reset", "work_dir": wd, "commits": commits}
                rows = [[InlineKeyboardButton(
                            f"{c['hash']} — {c['msg']}", callback_data=f"reset:{i}")]
                        for i, c in enumerate(commits)]
                await update.message.reply_text("↩️ Reset to which commit?",
                                                reply_markup=InlineKeyboardMarkup(rows))
                return

            label = f"gh {op}" if op else "gh"
            ack = await update.message.reply_text(
                f"⏳ {label} in `{Path(wd).name}`…", parse_mode="Markdown")
            result = truncate(await run_gh(args, wd))
            try:
                await ack.edit_text(
                    f"✅ *{MACHINE_NAME}* — {label} (`{Path(wd).name}`)\n\n{result}",
                    parse_mode="Markdown")
            except Exception:
                await ack.edit_text(f"✅ {MACHINE_NAME} — {label}\n\n{result}")
            return

        if cmd == "mkdir":
            ack = await update.message.reply_text("⏳ Creating…")
            result = await run_mkdir(args, str(WORK_DIR))
            await ack.edit_text(result)
            return

        if cmd:
            await update.message.reply_text(
                f"Unknown: `{cmd}`. Send /start for help.", parse_mode="Markdown")

    async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
        query = update.callback_query
        await query.answer()
        if not authorized(update):
            return

        chat_id = update.effective_chat.id
        data    = query.data or ""

        if data.startswith("proj:"):
            p       = pending.pop(chat_id, {})
            subdirs = p.get("subdirs", [])
            idx     = int(data[5:])
            if idx == 0:
                active_dirs[chat_id] = str(WORK_DIR)
                name = f"{WORK_DIR.name}/ (root)"
            else:
                name = subdirs[idx - 1]
                active_dirs[chat_id] = str(WORK_DIR / name)
            await query.edit_message_text(f"📌 Active project: *{name}*", parse_mode="Markdown")

        elif data.startswith("reset:"):
            p = pending.pop(chat_id, None)
            if not p:
                await query.edit_message_text("⚠️ Session expired. Run `gh reset` again.")
                return
            idx    = int(data[6:])
            commit = p["commits"][idx]
            result = await _git(["git", "reset", "--hard", commit["hash"]], p["work_dir"])
            await query.edit_message_text(
                f"↩️ Reset to `{commit['hash']}` — {commit['msg']}\n\n{result}",
                parse_mode="Markdown")

        elif data.startswith("perm_allow:") or data.startswith("perm_deny:"):
            req_id    = data.split(":", 1)[1]
            decision  = "allow" if data.startswith("perm_allow:") else "deny"
            req_file  = f"/tmp/claude_perm_{req_id}.request"
            resp_file = f"/tmp/claude_perm_{req_id}.response"
            if os.path.exists(req_file):
                with open(resp_file, "w") as f:
                    f.write(decision)
                icon = "✅" if decision == "allow" else "❌"
                await query.edit_message_text(
                    f"{icon} {decision.title()}ed permission `{req_id}`")
            else:
                await query.edit_message_text(
                    f"⚠️ Request `{req_id}` already resolved or expired.")

    async def handle_media(update: Update, context: ContextTypes.DEFAULT_TYPE):
        chat_id = update.effective_chat.id
        if not authorized(update):
            return

        msg = update.message
        caption = (msg.caption or "").strip()
        wd = active_dirs.get(chat_id)
        if not wd:
            await msg.reply_text(
                "📂 No active project. Send `proj` to pick one first.", parse_mode="Markdown")
            return

        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

        if msg.photo:
            file_id    = msg.photo[-1].file_id
            filename   = f"photo_{timestamp}.jpg"
            media_type = "photo"
        elif msg.video:
            file_id    = msg.video.file_id
            filename   = f"video_{timestamp}.mp4"
            media_type = "video"
        elif msg.video_note:
            file_id    = msg.video_note.file_id
            filename   = f"videonote_{timestamp}.mp4"
            media_type = "video"
        elif msg.document and msg.document.mime_type and \
                msg.document.mime_type.startswith("image/"):
            file_id    = msg.document.file_id
            ext        = (msg.document.file_name or "image.jpg").rsplit(".", 1)[-1]
            filename   = f"image_{timestamp}.{ext}"
            media_type = "photo"
        else:
            return

        save_path = Path(wd) / filename
        ack = await msg.reply_text(f"⏳ Downloading {media_type}…")

        try:
            tg_file = await context.bot.get_file(file_id)
            await tg_file.download_to_drive(str(save_path))
        except Exception as e:
            await ack.edit_text(f"❌ Download failed: {e}")
            return

        if media_type == "video":
            frames_dir = Path(wd) / f"frames_{timestamp}"
            frames_dir.mkdir(exist_ok=True)
            frame_files = []
            try:
                proc = await asyncio.create_subprocess_exec(
                    "ffmpeg", "-i", str(save_path), "-vf", "fps=1",
                    str(frames_dir / "frame_%03d.jpg"),
                    stdout=asyncio.subprocess.DEVNULL, stderr=asyncio.subprocess.DEVNULL)
                await asyncio.wait_for(proc.communicate(), timeout=120)
                frame_files = sorted(frames_dir.glob("frame_*.jpg"))
            except Exception:
                pass

            if frame_files:
                frames_list = "\n".join(f"`{f}`" for f in frame_files[:12])
                prompt = (
                    f"{caption}\n\n" if caption else ""
                ) + (
                    f"I sent you a video. I extracted frames (1 per second) saved in `{frames_dir}/`. "
                    f"Use the Read tool to view these frame images and describe what you see:\n{frames_list}"
                )
            else:
                prompt = (
                    f"{caption}\n\n" if caption else ""
                ) + (
                    f"I sent you a video saved at `{save_path}`. "
                    f"Note: ffmpeg frame extraction was unavailable. Describe the file if possible."
                )
        else:
            prompt = (
                f"{caption}\n\n" if caption else "Analyze this image. "
            ) + f"Use the Read tool to view and describe the image at `{save_path}`."

        await ack.edit_text(
            f"⏳ Claude analyzing {media_type} in `{Path(wd).name}`…", parse_mode="Markdown")
        result = truncate(await run_claude(prompt, wd, chat_id))
        sid    = sessions.get((chat_id, wd))
        header = f"✅ *{MACHINE_NAME}* — Vision (`{Path(wd).name}`)"
        header += f"  _{sid[:8]}_" if sid else ""
        try:
            await ack.edit_text(f"{header}\n\n{result}", parse_mode="Markdown")
        except Exception:
            await ack.edit_text(f"✅ {MACHINE_NAME} — Vision\n\n{result}")

    app.add_handler(CommandHandler("start", handle_start))
